#include<iostream>
using namespace std;

int main(){
    // //post increament i++
    // //pre increament i--
    int i=4;
    cout<<(++i)<<endl;//5
     cout<<(i++)<<endl;//5
     cout<<(i--)<<endl;//6
     cout<<(--i)<<endl;//4

     int a=1;
     int b=a++;
     int c=++a;
     cout<<b<<endl;
     cout<<c<<endl;
}